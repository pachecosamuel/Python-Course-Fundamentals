list1 = [2, 4, 6, 8];
list2 = [3, 5, 7, 9];
list3 = [2, 4, 6, 8];
var = 4;

# Concatenation between the list
print(list1 + list2);

# Multiplying the list for a number
# will repeat its sequence x times.
print(list1 * 10);

# You can compare the content of the list
# with another one using the operator ==
print(list1 == list2)
print(list1 == list3)

# you can look for a value inside a list using: value in list
print(var in list1);
